<template>
  <div class="my-tab-bar">
    <div
      class="tab-item"
      v-for="item in arr"
      :key="item.componentName"
      @click="active(item.componentName)"
      :class="{ current: current == item.componentName }"
    >
      <!-- 图标 -->
      <span class="iconfont" :class="item.iconText"></span>
      <!-- 文字 -->
      <span>{{ item.text }}</span>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      current: "MyGoodsList",
    };
  },
  props: {
    arr: {
      type: Array,
    },
  },
  methods: {
    active(val) {
      this.current = val;
      this.$emit("change", val);
    },
  },
};
</script>

